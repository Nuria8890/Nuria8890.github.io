# Tipos de datos
print("int:")
print(type(10))
print(type(0))
print(type(-5))
print(type(23456789765432))
print(type(23456789765432 + 1))

print("float:")
print(type(3.14))
print(type(1.0))
print(type(1e3))

print("complex:")
print(type(1 + 2j))

print("str:")
print(type("Hola"))
print(type("""
multilinea
"""))

print("bool:")
print(type(True))
print(type(False))
print(type(1 < 2))

print("NoneType:")
print(type(None))